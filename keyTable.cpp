const char *HidToText(unsigned char hid)
{
	for(int i = 0; i < sizeof(keyCode_table)/sizeof(keyCode_table[0]); i++)
	{
		if(keyCode_table[i].hid == hid)
			return keyCode_table[i].keyName;
	}
	return "";
}

unsigned char TextToHid(const char *str)
{
    for(int i = 0; i < sizeof(keyCode_table)/sizeof(keyCode_table[0]); i++)
	{
		if(strcmp(keyCode_table[i].keyName,str) == 0)
			return keyCode_table[i].hid;
	}
	return 0xFF;
}

unsigned char KeyToHid(int keyCode)
{
	for(int i = 0; i < sizeof(keyCode_table)/sizeof(keyCode_table[0]); i++)
	{
		if(keyCode_table[i].keyCode == keyCode)
			return keyCode_table[i].hid;
	}
	return 0xFF;
}